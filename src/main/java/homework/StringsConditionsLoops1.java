package src.main.java.homework;

public class StringsConditionsLoops1 {
    public static void main(String[] args) {

        System.out.println("The (While) method");

        int a = 6;

        while ( a <= 15 ) {

            System.out.println( a - 5 );

             a++;
        }

        System.out.println("The (for) method");



        for (int b = 6; b <= 15; b++) {
            System.out.println( b - 5 );
        }



    }
}
